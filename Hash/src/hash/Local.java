package hash;

public class Local {
  private int key;
  private int index;
  
  public Local (int key) {
	  this.key = key;
  }
  
  public void setKey(int key) {
	  this.key = key;
  }
  
  public int getKey() {
	  return this.key;
  }
  
  public void setIndex(int index) {
	  this.index = index;
  }
  
  public int getIndex() {
	  return this.index;
  }
}
